export function isValidGuess(word: string): boolean {
  return VALID_SET.has(word.toLowerCase());
}

const EPOCH = "2026-04-19";

export function getTodayStr(): string {
  const d = new Date();
  return `${d.getFullYear()}-${String(d.getMonth() + 1).padStart(2, "0")}-${String(d.getDate()).padStart(2, "0")}`;
}

export function getDayIndex(dateStr = getTodayStr()): number {
  const [ey, em, ed] = EPOCH.split("-").map(Number);
  const [dy, dm, dd] = dateStr.split("-").map(Number);
  const epoch = new Date(ey, em - 1, ed).getTime();
  const target = new Date(dy, dm - 1, dd).getTime();
  return Math.max(0, Math.floor((target - epoch) / 86400000));
}

export function getDailyWord(dateStr = getTodayStr()): string {
  const idx = getDayIndex(dateStr);
  return ANSWERS[idx % ANSWERS.length].toUpperCase();
}

export function getRandomWord(): string {
  return ANSWERS[Math.floor(Math.random() * ANSWERS.length)].toUpperCase();
}
